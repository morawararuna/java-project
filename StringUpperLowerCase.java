package arunaapplication;

public class StringUpperLowerCase {

	public static void main(String[] args) {
		//G--65,66
		//g----97,98
		
			char ch = 'G';
			ch = (char)(ch+32);
		System.out.println(ch);
		ch=(char)(ch-32);
		System.out.println(ch);
		char ch2='H';
		ch2=(char)(ch2+32);
		System.out.println(ch2);
		ch2=(char)(ch2-32);
		System.out.println(ch2);

	}

}
